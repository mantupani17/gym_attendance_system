    public void deleteAllData()
    {
        int res=JOptionPane.showConfirmDialog(null, "Are you Sure to Delete All Records ?","Delete All Data !",JOptionPane.YES_NO_OPTION);
        if(res==JOptionPane.YES_OPTION)
        {
        try{
            stm=con.createStatement();
            String sql="delete from Member_tab";
            int x=stm.executeUpdate(sql);
            if(x>0)
            {
                JOptionPane.showMessageDialog(null, "All records Are Deleted !");
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Database Is Empty !");
            }
        }catch(SQLException e)
        {
            JOptionPane.showMessageDialog(null, e.toString());
        }
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Delete Cancelled !");
        }
    }
