   public void deleteData()
   {int res=JOptionPane.showConfirmDialog(null, "Are you sure to delete this record ?", "delete Member", JOptionPane.YES_NO_OPTION);
   if(res==JOptionPane.YES_OPTION)
   {
       try{
           stm=con.createStatement();
           String sql="delete from Member_tab where Mid='"+jTextField1.getText()+"'";
           int x=stm.executeUpdate(sql);
           if(x>0)
           {
               JOptionPane.showMessageDialog(null, "delete successfully !", "delete Member", JOptionPane.INFORMATION_MESSAGE); 
           }
           else
           {
               JOptionPane.showMessageDialog(null, "Member Not Found","Delete Member",JOptionPane.ERROR_MESSAGE);
           }
       }catch(SQLException e)
       {
           JOptionPane.showMessageDialog(null, e.toString());
       }
   }
   else
   {
       JOptionPane.showMessageDialog(null, "Delete Cancelled !");
   }
   }
    
