    public void updateKey()//this method is used for set key value to the bill form
    {
        try{
            int key=Integer.parseInt(jTextField1.getText());
            String sql="update KeyTab set pass='"+new String(jPasswordField1.getPassword())+"' where key="+key+"";
            stm=con.createStatement();
            stm.execute(sql);
            JOptionPane.showMessageDialog(rootPane, "Key Changed Successfully..");
        }catch(Exception e){
        JOptionPane.showMessageDialog(rootPane, e.toString());
        }
    }
   public void viewKey()
   {
        try{
            int key=Integer.parseInt(jTextField1.getText());
            rs=con.createStatement().executeQuery("select pass from KeyTab where key="+key+"");
            if(rs.next())
            {
            JOptionPane.showMessageDialog(rootPane, rs.getString("pass"));
            }
        }catch(Exception e){}
   }
