    public void selectData()
    {
        try{
            int x=1;
            rs=con.createStatement().executeQuery("select * from Adv_tab where ID="+x+"");
            while(rs.next())
            {
                jTextField1.setText(rs.getString("Gname"));
                jTextField3.setText(rs.getString("Mno"));
                jTextField2.setText(rs.getString("Adder"));
            }
        }catch(SQLException e)
        {
            JOptionPane.showMessageDialog(null, e.toString());
        }
        
    }
    public void setData()
    {
        try{
            stm=con.createStatement();
            String sql="Update Adv_tab set Gname='"+jTextField1.getText()+"',Mno='"+jTextField3.getText()+"',Adder='"+jTextField2.getText()+"' where ID="+1+"";
            stm.execute(sql);
            JOptionPane.showMessageDialog(null, "advertisement Set !");
        }catch(SQLException e)
        {
            JOptionPane.showMessageDialog(null, e.toString());
        }   
    
    }
