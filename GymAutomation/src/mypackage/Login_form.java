    public void loginUser()
    {
      String sql;
      String ps=null,idd=null;
      String pass=new String(jPasswordField1.getPassword());
      try{
      try{
          String id=jTextField1.getText();
          
          sql="select ID,Pass from Login_tab where ID='"+id+"'";
          //stm=con.createStatement();
          rs=con.createStatement().executeQuery(sql);
          while(rs.next())
          {
          ps=rs.getString("Pass");
          idd=rs.getString("ID");
          }
          if(ps.equalsIgnoreCase(pass) && idd.equalsIgnoreCase(id) )
          {
            JOptionPane.showMessageDialog(null, "Login Successfully !");
            homePage hp=new homePage();
            hp.setVisible(rootPaneCheckingEnabled);
            hp.jMenu3.setEnabled(false);
            hp.jMenu4.setEnabled(false);
            this.dispose();        
          }
          else
          {
              JOptionPane.showMessageDialog(null, "invalid password or ID"+pass +"" +idd);
          }
          
      }catch(SQLException e)
      {
          JOptionPane.showMessageDialog(null, e.toString());
      }
      }
      catch(NullPointerException e)
      {
          JOptionPane.showMessageDialog(null, "User Not Exist !");
      }
    }
