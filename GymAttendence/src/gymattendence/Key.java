    public void setKey()//this method is used to set a key
    {
        try{
            String sql="insert into Key_tab values(?,?)";
            PreparedStatement pst=con.prepareStatement(sql);
            pst.setInt(1, Integer.parseInt(jTextField1.getText()));
            pst.setString(2, new String(jPasswordField1.getPassword()));
            pst.executeQuery();
            JOptionPane.showMessageDialog(rootPane,"Key set...!");
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, e.toString());
        }
    }
